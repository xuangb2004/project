    public ServiceInvoice fillTextClickTable() {
        int row = jTable2.getSelectedRow();
        ServiceInvoice serviceInvoice = null;
        if (row > -1) {
            int id = (int) jTable2.getValueAt(row, 0);
            String serviceId = (String) jTable2.getValueAt(row, 1);
            int roomID = (int) jTable2.getValueAt(row, 2);
            int quantity = (int) jTable2.getValueAt(row, 3);
            double price = (double) jTable2.getValueAt(row, 4);
            serviceInvoice = new ServiceInvoice(id, serviceId, roomID, quantity, price);
        }
        return serviceInvoice;
    }

