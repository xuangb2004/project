    public Booking getAllTextBooking() {
        Booking booking;
        int roomIdRoom = getRoomId();
     
        String quantity = jTextFieldQuantity.getText();
        String address = jTextFieldAddress.getText();
        Date dateCheckin = jDateCheckin.getDate();
        Date dateCheckOut = jDateCheckOut.getDate();
        Calendar calendarCheckin = Calendar.getInstance();
        calendarCheckin.setTime(dateCheckin);

        Calendar calendarCheckOut = Calendar.getInstance();
        calendarCheckOut.setTime(dateCheckOut);
        int hourCheckin = calendarCheckin.get(Calendar.HOUR_OF_DAY);
        int minuteCheckin = calendarCheckin.get(Calendar.MINUTE);
        int secondCheckin = calendarCheckin.get(Calendar.SECOND);

        int hourCheckOut = calendarCheckOut.get(Calendar.HOUR_OF_DAY);
        int minuteCheckOut = calendarCheckOut.get(Calendar.MINUTE);
        int secondCheckOut = calendarCheckOut.get(Calendar.SECOND);
        LocalDateTime checkinLocalDateTime = LocalDateTime.ofInstant(dateCheckin.toInstant(), ZoneId.systemDefault())
                .withHour(hourCheckin)
                .withMinute(minuteCheckin)
                .withSecond(secondCheckin);

        LocalDateTime checkoutLocalDateTime = LocalDateTime.ofInstant(dateCheckOut.toInstant(), ZoneId.systemDefault())
                .withHour(hourCheckOut)
                .withMinute(minuteCheckOut)
                .withSecond(secondCheckOut);
        String requiredSpecial = jTextFieldSpecial.getText();
        String rental = (String) jComboBoxRental.getSelectedItem();
        double price = 0;
        int fee = 0;
        if (jTextFieldFee.getText().isEmpty()) {
            fee = -1;
        } else {
            fee = Integer.valueOf(jTextFieldFee.getText());
        }
        if (jTextField1.getText().isEmpty()) {
            price = 0;
        } else {
            price = Double.valueOf(jTextField1.getText());
        }
        booking = new Booking(roomIdRoom, checkinLocalDateTime, checkoutLocalDateTime, quantity, requiredSpecial, fee, rental, price);
        return booking;
    }

