    public Customer getAllText() {
        Customer customer;
        String gender = "";
        if (jRadioButton1.isSelected()) {
            gender = "Nữ";
        } else if (jRadioButton2.isSelected()) {
            gender = "Nam";
        } else {
            gender = "Khác";
        }
        String name = jTextName.getText();
        String cccd = jTextFieldCCCD.getText();
        String fee = jTextFieldFee.getText();
        String numberPhone = jTextFieldNumberPhone.getText();
        String address = jTextFieldAddress.getText();
        int age = 0;
        if (jTextAge.getText().isEmpty()) {
            age = -1;
        } else {
            age = Integer.valueOf(jTextAge.getText());
        }
        customer = new Customer(name, cccd, numberPhone, address, gender, age);
        return customer;
    }

